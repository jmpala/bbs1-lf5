'''
Erstelle eine Funktion quadrat(a). Diese Funktion nimmt einen Parameter und liefert als Ergebnis das quadrierte Ergebnis. Teste diese Funktion nach der Erstellung mit Beispielwerten!
'''

def quadrat(num):
    return num * num

print("2 ^ 2 = ", quadrat(2))
print("3 ^ 2 = ", quadrat(3))
print("4 ^ 2 = ", quadrat(4))
print("5 ^ 2 = ", quadrat(5))